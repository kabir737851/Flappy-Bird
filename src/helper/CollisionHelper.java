package helper;

import model.Bird;
import model.Pipe;

public class CollisionHelper {
    public static boolean collision(Bird a, Pipe b) {
        return a.x < b.x + b.width &&
                a.x + a.width > b.x &&
                a.y < b.y + b.height &&
                a.y + a.height > b.y;
    }
}
